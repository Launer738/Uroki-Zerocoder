# ============================================
# ИМПОРТ МОДУЛЯ ДЛЯ АБСТРАКТНЫХ КЛАССОВ
# ============================================
# ABC - базовый класс для создания абстрактных классов
# abstractmethod - декоратор для создания абстрактных методов
# ============================================
from abc import ABC, abstractmethod

# ============================================
# АБСТРАКТНЫЙ КЛАСС Weapon (Оружие)
# ============================================
# Абстрактный класс - это "шаблон", который нельзя использовать напрямую
# Он определяет, какие методы должны быть у дочерних классов
# 
# Зачем нужен:
# - Гарантирует, что все типы оружия будут иметь метод attack()
# - Демонстрирует принцип Open/Closed: можно добавлять новое оружие,
#   не изменяя существующий код бойцов
# ============================================
class Weapon(ABC):  # ABC означает "Abstract Base Class" (абстрактный базовый класс)
    # ============================================
    # АБСТРАКТНЫЙ МЕТОД attack()
    # ============================================
    # @abstractmethod - декоратор, который делает метод абстрактным
    # Абстрактный метод - это метод БЕЗ реализации (только объявление)
    # 
    # Правило: все классы, наследующиеся от Weapon, ОБЯЗАНЫ реализовать attack()
    # Если не реализуют - будет ошибка при создании объекта
    # ============================================
    @abstractmethod
    def attack(self):
        pass  # pass означает "ничего не делай" - метод пустой, реализация в дочерних классах

# ============================================
# КЛАСС Sword (Меч) - наследуется от Weapon
# ============================================
# Наследование: Sword получает все от Weapon, но реализует attack() по-своему
# Это демонстрирует полиморфизм: один метод attack(), но разное поведение
# ============================================
class Sword(Weapon):  # Sword наследуется от Weapon
    def attack(self):
        # Реализуем абстрактный метод attack() для меча
        # Возвращаем строку с описанием атаки мечом
        return "наносит удар мечом"

# ============================================
# КЛАСС Bow (Лук) - наследуется от Weapon
# ============================================
# Аналогично Sword, но с другой реализацией attack()
# Можно добавлять сколько угодно типов оружия БЕЗ изменения других классов!
# ============================================
class Bow(Weapon):  # Bow тоже наследуется от Weapon
    def attack(self):
        # Реализуем attack() для лука - другое описание атаки
        return "наносит удар из лука"

# ============================================
# КЛАСС Monster (Монстр)
# ============================================
# Представляет врага в игре
# ============================================
class Monster():
    # ============================================
    # КОНСТРУКТОР __init__
    # ============================================
    # Вызывается автоматически при создании объекта Monster
    # Параметры:
    #   name - имя монстра
    #   health - здоровье монстра (количество HP)
    # ============================================
    def __init__(self, name, health):
        self.__name = name        # Приватный атрибут: имя монстра
        self.__health = health    # Приватный атрибут: здоровье монстра
    
    # ============================================
    # GET-МЕТОДЫ для доступа к приватным атрибутам
    # ============================================
    def get_name(self):
        return self.__name  # Возвращаем имя монстра
    
    def get_health(self):
        return self.__health  # Возвращаем текущее здоровье
    
    # ============================================
    # МЕТОД take_damage - получение урона
    # ============================================
    # Уменьшает здоровье монстра на указанное количество урона
    # Если здоровье становится <= 0, устанавливаем его в 0
    # ============================================
    def take_damage(self, damage):
        self.__health -= damage  # Уменьшаем здоровье на урон
        if self.__health <= 0:   # Если здоровье <= 0
            self.__health = 0    # Устанавливаем в 0 (не может быть отрицательным)
    
    # ============================================
    # МЕТОД is_alive - проверка, жив ли монстр
    # ============================================
    # Возвращает True, если здоровье > 0, иначе False
    # ============================================
    def is_alive(self):
        return self.__health > 0  # True если жив, False если мертв


# ============================================
# КЛАСС Fighter (Боец)
# ============================================
# Представляет игрока в игре
# Использует КОМПОЗИЦИЮ: содержит объект Weapon (оружие)
# ============================================
class Fighter():
    # ============================================
    # КОНСТРУКТОР __init__
    # ============================================
    # Создает бойца с именем, но БЕЗ оружия (оружие можно добавить позже)
    # ============================================
    def __init__(self, name):
        self.__name = name
        self.__weapon = None  # Пока оружия нет (None означает "ничего")
                              # Позже сюда будет помещен объект Weapon (Sword, Bow и т.д.)
    
    def get_name(self):
        return self.__name  # Возвращаем имя бойца
    
    # ============================================
    # МЕТОД change_weapon - смена оружия
    # ============================================
    # Позволяет бойцу сменить оружие
    # Параметр weapon - объект класса Weapon (Sword, Bow, Axe и т.д.)
    # 
    # Это демонстрирует принцип Open/Closed:
    # - Можно добавлять новое оружие (открыто для расширения)
    # - Не нужно изменять класс Fighter (закрыто для модификации)
    # ============================================
    def change_weapon(self, weapon):
        self.__weapon = weapon  # Сохраняем новое оружие в атрибут __weapon
    
    # ============================================
    # МЕТОД attack - атака монстра
    # ============================================
    # Боец атакует монстра используя текущее оружие
    # 
    # Полиморфизм в действии:
    # - self.__weapon.attack() вызывает метод attack() у оружия
    # - Но какое оружие? Sword, Bow, Axe? Не важно!
    # - Каждый класс оружия сам знает, как атаковать
    # ============================================
    def attack(self, monster):
        # Проверяем, есть ли у бойца оружие
        if self.__weapon is None:
            print(f"{self.__name} не имеет оружия!")
            return  # Выходим из метода, если оружия нет
        
        # ПОЛИМОРФИЗМ: вызываем attack() у оружия
        # Python сам знает, какой метод вызвать (Sword.attack() или Bow.attack())
        attack_description = self.__weapon.attack()
        print(f"{self.__name} {attack_description}")
        
        # Наносим урон монстру
        monster.take_damage(50)  # Например, 50 урона (можно сделать разным для разных оружий)


# ============================================
# ДЕМОНСТРАЦИЯ РАБОТЫ ПРОГРАММЫ
# ============================================

# Создаем бойца с именем "Герой"
fighter = Fighter("Герой")

# Создаем монстра с именем "Орк" и здоровьем 100
monster = Monster("Орк", 100)

# ============================================
# БОЙ С МЕЧОМ
# ============================================
# Боец выбирает меч
sword = Sword()                    # Создаем объект меча
fighter.change_weapon(sword)        # Боец берет меч в руки
print(f"{fighter.get_name()} выбирает меч.")
fighter.attack(monster)             # Боец атакует монстра мечом

# Проверяем результат боя
if not monster.is_alive():          # Если монстр не жив (health <= 0)
    print(f"{monster.get_name()} побежден!")
else:                                # Если монстр еще жив
    print(f"{monster.get_name()} еще жив! Здоровье: {monster.get_health()}")

# ============================================
# БОЙ С ЛУКОМ
# ============================================
# Боец меняет оружие на лук
bow = Bow()                         # Создаем объект лука
fighter.change_weapon(bow)          # Боец меняет меч на лук
print(f"\n{fighter.get_name()} выбирает лук.")
fighter.attack(monster)             # Боец атакует монстра из лука

if not monster.is_alive():
    print(f"{monster.get_name()} побежден!")

# ============================================
# ДЕМОНСТРАЦИЯ ПРИНЦИПА OPEN/CLOSED
# ============================================
# Добавляем НОВОЕ оружие (Axe) БЕЗ изменения существующего кода!
# 
# Что мы НЕ меняли:
# - Класс Fighter (остался без изменений)
# - Класс Monster (остался без изменений)
# - Механизм боя (остался без изменений)
#
# Что мы добавили:
# - Только новый класс Axe
#
# Это и есть принцип Open/Closed:
# - Открыто для расширения (можно добавлять новое оружие)
# - Закрыто для модификации (не нужно менять существующий код)
# ============================================
class Axe(Weapon):  # Новое оружие - добавляем БЕЗ изменения существующего кода!
    def attack(self):
        return "наносит удар топором"

# Используем новое оружие - работает сразу, без изменений в Fighter!
axe = Axe()                         # Создаем объект топора
fighter.change_weapon(axe)          # Боец берет топор
print(f"\n{fighter.get_name()} выбирает топор.")
fighter.attack(monster)             # Боец атакует топором - работает!

