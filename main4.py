class Test():
    def __init__(self):
        self.public = "публичный атрибут"
        self._protected = "защищенный атрибут"
        self.__private = "приватный атрибут"


    def get_private(self):
        return self.__private

    # ============================================
    # МЕТОД set_private - для установки нового значения приватного атрибута
    # ============================================
    # value - это ПАРАМЕТР метода (можно назвать как угодно: value, new_value, data и т.д.)
    # 
    # Зачем нужен value?
    # - Когда вы вызываете метод set_private(НОВОЕ_ЗНАЧЕНИЕ), это значение попадает в параметр value
    # - Метод использует value, чтобы установить новое значение для self.__private
    # 
    # Почему value?
    # - Это просто имя параметра (можно назвать как угодно: new_value, data, x и т.д.)
    # - value означает "значение", которое мы хотим установить
    # 
    # Пример использования:
    # test.set_private("новое значение")  # "новое значение" попадет в параметр value
    # ============================================
    def set_private(self, привет):
        # value - это то значение, которое передали при вызове метода
        # Например: test.set_private("привет") → value = "привет"
        # self.__private = value означает "установить приватный атрибут равным value"
        self.__private = привет

test = Test()
