# ============================================
# ИГРА "ЗМЕЙКА" НА PYGAME
# ============================================
# Классическая игра, где змейка растет, поедая еду
#
# ВАЖНО: Объекты рисуются БЕЗ ИЗОБРАЖЕНИЙ!
# Используются простые геометрические фигуры:
# - pygame.draw.rect() - прямоугольники
# - pygame.draw.circle() - круги
# - pygame.draw.line() - линии
#
# Альтернатива (с изображениями):
# - pygame.image.load("snake.png") - загрузка из файла
# ============================================

import pygame
import random

# ============================================
# ИНИЦИАЛИЗАЦИЯ PYGAME
# ============================================
# pygame.init() - запускает все модули pygame
# Без этого ничего не будет работать!
# ============================================
pygame.init()

# ============================================
# НАСТРОЙКИ ИГРЫ (КОНСТАНТЫ)
# ============================================
# Константы - это переменные, которые не меняются
# Обычно пишутся БОЛЬШИМИ БУКВАМИ
# ============================================
WINDOW_WIDTH = 800   # Ширина окна в пикселях
WINDOW_HEIGHT = 600  # Высота окна в пикселях
CELL_SIZE = 20       # Размер одной клетки (змейка движется по сетке)
FPS = 10             # Скорость игры (кадров в секунду)

# Цвета (RGB - Red, Green, Blue, значения от 0 до 255)
BLACK = (0, 0, 0)           # Черный фон
WHITE = (255, 255, 255)     # Белый
GREEN = (0, 255, 0)         # Зеленый (змейка)
RED = (255, 0, 0)           # Красный (еда)
BLUE = (0, 0, 255)          # Синий (голова змейки)

# ============================================
# СОЗДАНИЕ ОКНА ИГРЫ
# ============================================
# pygame.display.set_mode() - создает окно игры
# Возвращает объект Surface (поверхность для рисования)
# ============================================
screen = pygame.display.set_mode((WINDOW_WIDTH, WINDOW_HEIGHT))
pygame.display.set_caption("Змейка")  # Название окна

# Создаем объект Clock для управления FPS (частотой кадров)
clock = pygame.time.Clock()

# ============================================
# КЛАСС ЗМЕЙКИ
# ============================================
# Змейка - это список координат [x, y]
# Каждый элемент списка - это один сегмент змейки
# ============================================
class Snake:
    def __init__(self):
        # Начальная позиция змейки (середина экрана)
        start_x = WINDOW_WIDTH // 2
        start_y = WINDOW_HEIGHT // 2
        
        # Змейка начинается с 3 сегментов (голова + 2 части тела)
        # Каждый сегмент - это список [x, y]
        self.body = [
            [start_x, start_y],                    # Голова
            [start_x - CELL_SIZE, start_y],         # Тело 1
            [start_x - 2 * CELL_SIZE, start_y]     # Тело 2
        ]
        
        # Направление движения (по умолчанию вправо)
        # W, S, A, D - это строки для направления (WASD управление)
        self.direction = "D"  # D - вправо
        
        # Новое направление (для плавной смены)
        self.new_direction = "D"  # D - вправо
    
    # ============================================
    # МЕТОД move - движение змейки
    # ============================================
    def move(self):
        # Обновляем направление
        self.direction = self.new_direction
        
        # Вычисляем новую позицию головы в зависимости от направления
        head = self.body[0].copy()  # Копируем текущую голову
        
        if self.direction == "W":
            head[1] -= CELL_SIZE  # Движение вверх (уменьшаем Y)
        elif self.direction == "S":
            head[1] += CELL_SIZE  # Движение вниз (увеличиваем Y)
        elif self.direction == "A":
            head[0] -= CELL_SIZE  # Движение влево (уменьшаем X)
        elif self.direction == "D":
            head[0] += CELL_SIZE  # Движение вправо (увеличиваем X)
        
        # Добавляем новую голову в начало списка
        self.body.insert(0, head)
        
        # Удаляем последний сегмент (хвост)
        # Если змейка съела еду, этот сегмент не удалится (см. метод grow)
        self.body.pop()
    
    # ============================================
    # МЕТОД grow - рост змейки
    # ============================================
    # Вызывается, когда змейка съедает еду
    # Добавляет один сегмент в конец
    # ============================================
    def grow(self):
        # Просто добавляем копию последнего сегмента в конец
        # При следующем move() он не удалится, и змейка вырастет
        self.body.append(self.body[-1].copy())
    
    # ============================================
    # МЕТОД check_collision - проверка столкновений
    # ============================================
    # Проверяет:
    # 1. Столкновение со стенами
    # 2. Столкновение с собственным телом
    # ============================================
    def check_collision(self):
        head = self.body[0]
        
        # Проверка столкновения со стенами
        if (head[0] < 0 or head[0] >= WINDOW_WIDTH or
            head[1] < 0 or head[1] >= WINDOW_HEIGHT):
            return True  # Столкновение со стеной!
        
        # Проверка столкновения с собственным телом
        # Проверяем, не совпадает ли голова с каким-либо сегментом тела
        if head in self.body[1:]:  # body[1:] - все сегменты кроме головы
            return True  # Столкновение с собой!
        
        return False  # Столкновений нет
    
    # ============================================
    # МЕТОД draw - рисование змейки БЕЗ ИЗОБРАЖЕНИЙ
    # ============================================
    # В pygame можно рисовать объекты двумя способами:
    # 1. Простые геометрические фигуры (rect, circle, line) - БЕЗ файлов изображений
    # 2. Загрузка изображений из файлов (pygame.image.load()) - С файлами
    #
    # Здесь используется способ 1 - простые прямоугольники!
    # ============================================
    def draw(self, surface):
        for i, segment in enumerate(self.body):
            # i - индекс сегмента (0 для головы, 1, 2, 3... для тела)
            # segment - координаты [x, y] сегмента
            
            # Голова рисуется синим цветом
            if i == 0:
                color = BLUE
            else:
                color = GREEN  # Тело рисуется зеленым
            
            # ============================================
            # pygame.draw.rect() - РИСУЕТ ПРЯМОУГОЛЬНИК БЕЗ ИЗОБРАЖЕНИЯ
            # ============================================
            # Параметры:
            #   surface - где рисовать (экран игры)
            #   color - цвет прямоугольника (RGB кортеж)
            #   [x, y, width, height] - координаты и размеры
            #
            # Это НЕ изображение из файла!
            # Это простая геометрическая фигура, нарисованная кодом
            # ============================================
            pygame.draw.rect(surface, color, 
                           [segment[0], segment[1], CELL_SIZE, CELL_SIZE])

# ============================================
# КЛАСС ЕДЫ
# ============================================
class Food:
    def __init__(self):
        # Генерируем случайную позицию для еды
        self.position = self.generate_position()
    
    # ============================================
    # МЕТОД generate_position - генерация позиции
    # ============================================
    # Генерирует случайную позицию на сетке
    # ============================================
    def generate_position(self):
        # Вычисляем количество клеток по ширине и высоте
        cells_x = WINDOW_WIDTH // CELL_SIZE
        cells_y = WINDOW_HEIGHT // CELL_SIZE
        
        # Генерируем случайные координаты на сетке
        x = random.randint(0, cells_x - 1) * CELL_SIZE
        y = random.randint(0, cells_y - 1) * CELL_SIZE
        
        return [x, y]
    
    # ============================================
    # МЕТОД draw - рисование еды БЕЗ ИЗОБРАЖЕНИЯ
    # ============================================
    # Тоже используем pygame.draw.rect() - простой красный квадрат
    # Без загрузки файла изображения!
    # ============================================
    def draw(self, surface):
        # Рисуем красный квадрат (еду) - простая геометрическая фигура
        # Не нужно загружать файл изображения!
        pygame.draw.rect(surface, RED,
                        [self.position[0], self.position[1], 
                         CELL_SIZE, CELL_SIZE])

# ============================================
# ГЛАВНАЯ ФУНКЦИЯ ИГРЫ
# ============================================
def main():
    # Создаем объекты змейки и еды
    snake = Snake()
    food = Food()
    
    # Счет игры
    score = 0
    
    # Флаг для игрового цикла
    running = True
    game_over = False
    
    # ============================================
    # ГЛАВНЫЙ ИГРОВОЙ ЦИКЛ
    # ============================================
    # Этот цикл работает, пока игра запущена
    # ============================================
    while running:
        # ============================================
        # ОБРАБОТКА СОБЫТИЙ
        # ============================================
        # pygame.event.get() - получает все события (нажатия клавиш, закрытие окна и т.д.)
        # ============================================
        for event in pygame.event.get():
            # Событие закрытия окна
            if event.type == pygame.QUIT:
                running = False  # Выходим из цикла
            
            # Событие нажатия клавиши
            if event.type == pygame.KEYDOWN:
                # Если игра окончена, нажатие любой клавиши перезапускает игру
                if game_over:
                    snake = Snake()
                    food = Food()
                    score = 0
                    game_over = False
                else:
                    # Управление змейкой на WASD
                    # Проверяем, чтобы нельзя было развернуться на 180 градусов
                    if event.key == pygame.K_w and snake.direction != "S":
                        snake.new_direction = "W"  # W - вверх
                    elif event.key == pygame.K_s and snake.direction != "W":
                        snake.new_direction = "S"  # S - вниз
                    elif event.key == pygame.K_a and snake.direction != "D":
                        snake.new_direction = "A"  # A - влево
                    elif event.key == pygame.K_d and snake.direction != "A":
                        snake.new_direction = "D"  # D - вправо
        
        # ============================================
        # ОБНОВЛЕНИЕ ИГРЫ (если игра не окончена)
        # ============================================
        if not game_over:
            # Двигаем змейку
            snake.move()
            
            # Проверяем столкновения
            if snake.check_collision():
                game_over = True  # Игра окончена!
            
            # Проверяем, съела ли змейка еду
            # Если голова змейки совпадает с позицией еды
            if snake.body[0] == food.position:
                snake.grow()  # Змейка растет
                score += 10   # Увеличиваем счет
                food = Food()  # Создаем новую еду
                
                # Проверяем, чтобы новая еда не появилась на змейке
                while food.position in snake.body:
                    food = Food()
        
        # ============================================
        # ОТРИСОВКА (РИСОВАНИЕ)
        # ============================================
        # Заполняем экран черным цветом (очищаем предыдущий кадр)
        screen.fill(BLACK)
        
        # Рисуем змейку и еду
        snake.draw(screen)
        food.draw(screen)
        
        # Отображаем счет
        # pygame.font - модуль для работы со шрифтами
        font = pygame.font.Font(None, 36)  # None - системный шрифт, 36 - размер
        score_text = font.render(f"Счет: {score}", True, WHITE)
        screen.blit(score_text, (10, 10))  # blit - копирует текст на экран
        
        # Если игра окончена, показываем сообщение
        if game_over:
            game_over_font = pygame.font.Font(None, 72)
            game_over_text = game_over_font.render("ИГРА ОКОНЧЕНА!", True, RED)
            text_rect = game_over_text.get_rect(center=(WINDOW_WIDTH // 2, WINDOW_HEIGHT // 2))
            screen.blit(game_over_text, text_rect)
            
            restart_font = pygame.font.Font(None, 36)
            restart_text = restart_font.render("Нажмите любую клавишу для перезапуска", True, WHITE)
            restart_rect = restart_text.get_rect(center=(WINDOW_WIDTH // 2, WINDOW_HEIGHT // 2 + 50))
            screen.blit(restart_text, restart_rect)
        
        # ============================================
        # ОБНОВЛЕНИЕ ЭКРАНА
        # ============================================
        # pygame.display.update() - обновляет экран, показывая все, что нарисовано
        pygame.display.update()
        
        # Контроль FPS (частоты кадров)
        # clock.tick(FPS) - задерживает выполнение, чтобы игра работала с нужной скоростью
        clock.tick(FPS)
    
    # ============================================
    # ЗАВЕРШЕНИЕ ИГРЫ
    # ============================================
    # pygame.quit() - закрывает все модули pygame
    pygame.quit()

# ============================================
# ЗАПУСК ИГРЫ
# ============================================
# Если файл запущен напрямую (не импортирован), запускаем игру
# ============================================
if __name__ == "__main__":
    main()
