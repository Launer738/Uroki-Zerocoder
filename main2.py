# ============================================
# КЛАСС - это шаблон (чертеж) для создания объектов
# Класс описывает, какие данные и действия будут у объекта
# ============================================
class Warrior():
    # ============================================
    # __init__ - это специальный метод-конструктор
    # Он автоматически вызывается при создании нового объекта
    # Например: war1 = Warrior(...) → автоматически вызывается __init__
    # ============================================
    def __init__(self, name, power, endurance, hair_color):
        # ============================================
        # SELF - это очень важное понятие!
        # 
        # self - это ссылка на КОНКРЕТНЫЙ объект (экземпляр класса)
        # Когда вы создаете war1 = Warrior(...), self внутри war1 будет указывать на war1
        # Когда вы создаете war2 = Warrior(...), self внутри war2 будет указывать на war2
        # 
        # self.name означает "имя ЭТОГО конкретного воина"
        # self.power означает "сила ЭТОГО конкретного воина"
        # 
        # БЕЗ self Python не поймет, к какому объекту относится переменная!
        # 
        # ВАЖНО: self нужно указывать в КАЖДОМ методе класса (кроме специальных случаев)
        # self всегда должен быть ПЕРВЫМ параметром в методах класса
        # ============================================
        
        # Сохраняем переданные значения в атрибуты объекта
        # self.name - это атрибут (переменная) конкретного объекта
        self.name = name          # Имя этого воина
        self.power = power        # Сила этого воина
        self.endurance = endurance # Выносливость этого воина
        self.hair_color = hair_color # Цвет волос этого воина

    # ============================================
    # МЕТОД - это функция внутри класса
    # Методы описывают действия, которые может выполнять объект
    # ============================================
    def sleep(self):
        # self нужен здесь, чтобы получить доступ к атрибутам ЭТОГО объекта
        # self.name - имя того воина, который вызвал метод sleep()
        print(f"{self.name} лег спать")
        # self.endurance - выносливость того воина, который вызвал метод
        # += означает "увеличить на" (не путать с =+, который неправильный!)
        # ВАЖНО: нельзя писать p = self.endurance += 2
        # Оператор += не возвращает значение, его нужно использовать отдельно!
        self.endurance += 2  # Увеличиваем выносливость на 2
        print(f"Выносливость увеличена на 2, теперь она равна {self.endurance}")

    def eat(self):
        print(f"{self.name} сел кушать")
        # Увеличиваем силу на 1
        self.power += 1

    def hit(self):
        print(f"{self.name} бьет кого-то")
        # -= означает "уменьшить на"
        self.endurance -= 6  # Уменьшаем выносливость на 6

    def walk(self):
        # Этот метод просто выводит информацию, не изменяя атрибуты
        print(f"{self.name} гуляет")

    def info(self):
        # Метод для вывода всей информации о воине
        # self.name, self.hair_color и т.д. - обращаемся к атрибутам через self
        print(f"имя воина - {self.name}")
        print(f"цвет волос воина - {self.hair_color}")
        print(f"сила воина - {self.power}")
        print(f"выносливость воина - {self.endurance}")

# ============================================
# СОЗДАНИЕ ОБЪЕКТА (экземпляра класса)
# ============================================
# Когда мы пишем Warrior(...), происходит:
# 1. Создается новый объект (новый воин)
# 2. Автоматически вызывается __init__ с переданными параметрами
# 3. self внутри __init__ указывает на этот новый объект
# 4. Объект сохраняется в переменную war1
# ============================================
war1 = Warrior(name="Степа", power=76, endurance=54, hair_color="коричневый")
war2 = Warrior(name="Олег", power=50, endurance=24, hair_color="Лиловый")

# Выводим сам объект (покажет адрес в памяти, не очень полезно)

war2.sleep()
